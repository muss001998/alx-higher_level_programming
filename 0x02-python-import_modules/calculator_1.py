#!/usr/bin/python3
def add(a, b):
    """Here is my addition function

    Args:
        a: first int
        b: second int

    Returns:
        The return value. a + b
    """
    return (a + b)


def sub(a, b):
    """Here is my subtraction function

    Args:
        a: first int
        b: second int

    Returns:
        The return value. a - b
    """
    return (a - b)


def mul(a, b):
    """Here is my multiplication function

    Args:
        a: first int
        b: second int

    Returns:
        The return value. a * b
    """
    return (a * b)


def div(a, b):
    """Here is my division function

    Args:
        a: first int
        b: second int

    Returns:
	 The return value. a / b
    """
    return int(a / b)
